#  Program to sort the elements of given list in Ascending and Descending Order

my_list = [6, 4, 2, 8, 1, 3, 5, 7]

# Sort the list in ascending order (default)
my_list.sort()
print("Ascending order:", my_list)  # Output: Ascending order: [1, 2, 3, 4, 5, 6, 7, 8]

# Sort the list in descending order using reverse=True
my_list.sort(reverse=True)
print("Descending order:", my_list)  # Output: Descending order: [8, 7, 6, 5, 4, 3, 2, 1]


